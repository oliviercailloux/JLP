package io.github.oliviercailloux.jlp.elements;

/**
 * Defines the domain of a {@link Variable}.
 *
 * @author Olivier Cailloux
 *
 */
public enum VariableDomain {

	/**
	 * Integer domain, corresponding to a variable kind INT or BOOL.
	 */
	INT_DOMAIN,
	/**
	 * Real domain.
	 */
	REAL_DOMAIN;
}
